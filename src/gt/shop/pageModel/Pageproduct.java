package gt.shop.pageModel;

public class Pageproduct {

	private String product_id;
	private String product_url;
	private String product_name;
	private String product_description;
	private String product_shop;
	private String product_shopaddress;
	private double product_price;

	public String getProduct_id() {
		return product_id;
	}

	public void setProduct_id(String product_id) {
		this.product_id = product_id;
	}

	public String getProduct_url() {
		return product_url;
	}

	public void setProduct_url(String product_url) {
		this.product_url = product_url;
	}

	public String getProduct_name() {
		return product_name;
	}

	public void setProduct_name(String product_name) {
		this.product_name = product_name;
	}

	public String getProduct_description() {
		return product_description;
	}

	public void setProduct_description(String product_description) {
		this.product_description = product_description;
	}

	public String getProduct_shop() {
		return product_shop;
	}

	public void setProduct_shop(String product_shop) {
		this.product_shop = product_shop;
	}

	public String getProduct_shopaddress() {
		return product_shopaddress;
	}

	public void setProduct_shopaddress(String product_shopaddress) {
		this.product_shopaddress = product_shopaddress;
	}

	public double getProduct_price() {
		return product_price;
	}

	public void setProduct_price(double product_price) {
		this.product_price = product_price;
	}

}
